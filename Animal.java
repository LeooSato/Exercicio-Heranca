package br.com.generation.aula06.lista;

public class Animal {

	private String nome;
	private int idade;
	private String emiteSom;
	private String cor;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	public String getSom() {
		return emiteSom;
	}
	public void setSom(String som) {
		this.emiteSom = som;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	
	
	
}
